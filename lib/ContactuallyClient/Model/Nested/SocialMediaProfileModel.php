<?php

namespace ContactuallyClient\Model\Nested;

/**
 * Class SocialMediaProfileModel
 * @package ContactuallyClient\Model\Nested
 */
class SocialMediaProfileModel
{
    /**
     * @var string
     */
    private $id;

    /**
     * @var string
     */
    private $label;

    /**
     * @var string
     */
    private $url;

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     * @return SocialMediaProfileModel
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param string $label
     * @return SocialMediaProfileModel
     */
    public function setLabel($label)
    {
        $this->label = $label;
        return $this;
    }

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     * @return SocialMediaProfileModel
     */
    public function setUrl($url)
    {
        $this->url = $url;
        return $this;
    }
}